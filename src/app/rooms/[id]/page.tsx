'use client';

import { useState, useEffect, useCallback, useRef } from 'react';
import Link from 'next/link';
import { useParams } from 'next/navigation';
import { useAuth } from '@/lib/auth';
import LoadingSkeleton from '@/components/LoadingSkeleton';
import CallRoom from '@/components/CallRoom';
import { Phone } from 'lucide-react';

interface RoomMsg {
  id: string;
  roomId: string;
  senderId: string;
  senderName: string;
  text: string;
  createdAt: string;
}

interface RoomMember {
  userId: string;
  userName: string;
  role: string;
  joinedAt: string;
}

interface Room {
  id: string;
  name: string;
  topic: string;
  description: string;
  creatorId: string;
  maxMembers: number;
  status: string;
}

export default function RoomDetailPage() {
  const { id } = useParams<{ id: string }>();
  const { user } = useAuth();
  const [room, setRoom] = useState<Room | null>(null);
  const [members, setMembers] = useState<RoomMember[]>([]);
  const [messages, setMessages] = useState<RoomMsg[]>([]);
  const [loading, setLoading] = useState(true);
  const [text, setText] = useState('');
  const [sending, setSending] = useState(false);
  const [isMember, setIsMember] = useState(false);
  const [joining, setJoining] = useState(false);
  const [inCall, setInCall] = useState(false);
  const messagesEndRef = useRef<HTMLDivElement>(null);
  const pollRef = useRef<ReturnType<typeof setInterval>>();

  const myName = user?.email?.split('@')[0] || 'Student';

  const loadRoom = useCallback(async () => {
    if (!user || !id) return;
    try {
      const res = await fetch(`/api/rooms/${id}`);
      const data = await res.json();
      if (data.success) {
        setRoom(data.data.room);
        setMembers(data.data.members || []);
        setMessages(data.data.messages || []);
        setIsMember(
          (data.data.members || []).some((m: RoomMember) => m.userId === user.id)
        );
      }
    } catch (err) {
      console.error('loadRoom:', err);
    } finally {
      setLoading(false);
    }
  }, [user, id]);

  useEffect(() => {
    loadRoom();
  }, [loadRoom]);

  useEffect(() => {
    if (isMember) {
      pollRef.current = setInterval(loadRoom, 3000);
    }
    return () => {
      if (pollRef.current) clearInterval(pollRef.current);
    };
  }, [isMember, loadRoom]);

  useEffect(() => {
    messagesEndRef.current?.scrollIntoView({ behavior: 'smooth' });
  }, [messages]);

  const handleJoin = async () => {
    if (!user) return;
    setJoining(true);
    try {
      const res = await fetch(`/api/rooms/${id}`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ action: 'join', userId: user.id, userName: myName }),
      });
      const data = await res.json();
      if (data.success) await loadRoom();
    } catch (err) {
      console.error('joinRoom:', err);
    } finally {
      setJoining(false);
    }
  };

  const handleLeave = async () => {
    if (!user) return;
    try {
      await fetch(`/api/rooms/${id}`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({ action: 'leave', userId: user.id }),
      });
      await loadRoom();
    } catch (err) {
      console.error('leaveRoom:', err);
    }
  };

  const handleSend = async () => {
    if (!user || !text.trim()) return;
    setSending(true);
    try {
      await fetch(`/api/rooms/${id}`, {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          action: 'send_message',
          userId: user.id,
          userName: myName,
          text: text.trim(),
        }),
      });
      setText('');
      await loadRoom();
    } catch (err) {
      console.error('sendMessage:', err);
    } finally {
      setSending(false);
    }
  };

  if (loading) return <LoadingSkeleton />;
  if (!room) {
    return (
      <div className="max-w-4xl mx-auto p-6 text-center">
        <p className="text-[var(--muted)] text-lg">Room not found.</p>
        <Link href="/rooms" className="text-[var(--primary-light)] hover:underline">‚Üê Back to Rooms</Link>
      </div>
    );
  }

  // Full-screen call mode
  if (inCall) {
    return (
      <div className="h-[calc(100vh-60px)]">
        <CallRoom
          roomName={`room_${id}`}
          displayName={user?.name || myName}
          onLeave={() => setInCall(false)}
        />
      </div>
    );
  }

  return (
    <div className="max-w-5xl mx-auto p-4 md:p-6 grid grid-cols-1 md:grid-cols-[1fr_250px] gap-4 h-[calc(100vh-120px)]">
      {/* Main Chat Area */}
      <div className="flex flex-col bg-[var(--surface)] rounded-xl border border-[var(--border)] overflow-hidden">
        {/* Room Header */}
        <div className="p-4 border-b border-[var(--border)] flex items-center justify-between">
          <div>
            <div className="flex items-center gap-2">
              <Link href="/rooms" className="text-[var(--muted)] hover:text-[var(--foreground)]">‚Üê</Link>
              <h1 className="text-lg font-bold text-[var(--foreground)]">{room.name}</h1>
              <span className={`text-xs px-2 py-0.5 rounded-full ${
                room.status === 'active'
                  ? 'bg-[var(--success)]/15 text-[var(--success)]'
                  : 'bg-[var(--surface-light)] text-[var(--muted)]'
              }`}>{room.status}</span>
            </div>
            {room.topic && (
              <p className="text-sm text-[var(--muted)] ml-6">{room.topic}</p>
            )}
          </div>
          <div className="flex items-center gap-2">
            {isMember && (
              <>
                <button
                  onClick={() => setInCall(true)}
                  className="flex items-center gap-1.5 px-3 py-1.5 rounded-lg bg-[var(--success)]/15 text-[var(--success)] hover:bg-[var(--success)]/25 text-xs font-medium transition-colors"
                  title="Start voice/video call"
                >
                  <Phone size={14} /> Call
                </button>
                <button
                  onClick={handleLeave}
                  className="text-xs text-[var(--error)] hover:underline"
                >
                  Leave
                </button>
              </>
            )}
          </div>
        </div>

        {/* Messages */}
        {isMember ? (
          <>
            <div className="flex-1 overflow-y-auto p-4 space-y-3 min-h-0">
              {messages.length === 0 ? (
                <p className="text-center text-[var(--muted)] py-8">
                  No messages yet. Start the conversation! üí¨
                </p>
              ) : (
                messages.map((msg) => {
                  const isMe = msg.senderId === user?.id;
                  return (
                    <div
                      key={msg.id}
                      className={`flex ${isMe ? 'justify-end' : 'justify-start'}`}
                    >
                      <div
                        className={`max-w-[75%] px-4 py-2 rounded-2xl ${
                          isMe
                            ? 'bg-[var(--primary)] text-white rounded-br-md'
                            : 'bg-[var(--surface-light)] text-[var(--foreground)] rounded-bl-md'
                        }`}
                      >
                        {!isMe && (
                          <p className="text-xs font-semibold text-[var(--primary-light)] mb-0.5">
                            {msg.senderName}
                          </p>
                        )}
                        <p className="text-sm">{msg.text}</p>
                        <p className={`text-[10px] mt-1 ${isMe ? 'text-white/60' : 'text-[var(--muted)]'}`}>
                          {new Date(msg.createdAt).toLocaleTimeString([], { hour: '2-digit', minute: '2-digit' })}
                        </p>
                      </div>
                    </div>
                  );
                })
              )}
              <div ref={messagesEndRef} />
            </div>

            {/* Input */}
            <div className="p-3 border-t border-[var(--border)] flex gap-2">
              <input
                value={text}
                onChange={(e) => setText(e.target.value)}
                onKeyDown={(e) => e.key === 'Enter' && !e.shiftKey && handleSend()}
                placeholder="Type a message..."
                className="flex-1 px-4 py-2 rounded-full border border-[var(--border)] bg-[var(--surface-light)] text-[var(--foreground)] text-sm placeholder:text-[var(--muted)]"
              />
              <button
                onClick={handleSend}
                disabled={sending || !text.trim()}
                className="px-5 py-2 bg-[var(--primary)] text-white rounded-full text-sm font-medium hover:bg-[#6d28d9] disabled:opacity-50"
              >
                Send
              </button>
            </div>
          </>
        ) : (
          <div className="flex-1 flex flex-col items-center justify-center p-8 text-center">
            <p className="text-4xl mb-4">üîí</p>
            <p className="text-[var(--muted)] mb-4">
              Join this room to chat with other students
            </p>
            <button
              onClick={handleJoin}
              disabled={joining}
              className="px-6 py-2.5 bg-[var(--primary)] text-white rounded-lg font-medium hover:bg-[#6d28d9] disabled:opacity-50"
            >
              {joining ? 'Joining...' : `Join Room (${members.length}/${room.maxMembers})`}
            </button>
          </div>
        )}
      </div>

      {/* Members Sidebar */}
      <div className="bg-[var(--surface)] rounded-xl border border-[var(--border)] p-4 space-y-4 hidden md:block">
        <h2 className="font-bold text-[var(--foreground)]">
          Members ({members.length}/{room.maxMembers})
        </h2>
        <div className="space-y-2">
          {members.map((m) => (
            <div
              key={m.userId}
              className="flex items-center gap-2 text-sm"
            >
              <div className="w-8 h-8 rounded-full bg-[var(--primary)]/15 flex items-center justify-center text-[var(--primary-light)] font-bold text-xs">
                {m.userName?.charAt(0)?.toUpperCase() || '?'}
              </div>
              <div>
                <p className="text-[var(--foreground)]">
                  {m.userName}
                  {m.userId === user?.id && (
                    <span className="text-xs text-[var(--muted)] ml-1">(you)</span>
                  )}
                </p>
                {m.role === 'creator' && (
                  <span className="text-[10px] text-amber-500">Creator</span>
                )}
              </div>
            </div>
          ))}
        </div>

        {room.description && (
          <>
            <hr className="border-[var(--border)]" />
            <div>
              <h3 className="text-xs uppercase text-[var(--muted)] mb-1">About</h3>
              <p className="text-sm text-[var(--muted)]">{room.description}</p>
            </div>
          </>
        )}
      </div>
    </div>
  );
}
